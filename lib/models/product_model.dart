class ProductModel {
  final String imgUrl;
  final String? text;
  final String? description;
  final String? details;
  final int? offer;
  final int? offerPrice;
  final int? price;
  final String? rating;
  final String? star;
  final String? people;
  ProductModel({
    required this.imgUrl,
     this.text,
    this.description,
    this.details,
    this.offer,
    this.offerPrice,
     this.price,
     this.rating,
    this.star,
    this.people,
  });
}
